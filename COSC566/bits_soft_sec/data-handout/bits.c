/*
 * bitAnd - x&y using only ~ and |
 *   Example: bitAnd(6, 5) = 4
 *   Legal ops: ~ |
 *   Max ops: 8
 *   Rating: 1
 */
int bitAnd(int x, int y) {
  return ~(~x | ~y);
  // demorgans law not(x&y) = not x or not y -> x & y = not(not x or not y)
}
/*
 * bitNor - ~(x|y) using only ~ and &
 *   Example: bitNor(0x6, 0x5) = 0xFFFFFFF8
 *   Legal ops: ~ &
 *   Max ops: 8
 *   Rating: 1
 */
int bitNor(int x, int y) {
  return (~x & ~y);
  // demorgans law not(x | y) = not x and not y
}
/*
 * copyLSB - set all bits of result to least significant bit of x
 *   Example: copyLSB(5) = 0xFFFFFFFF, copyLSB(6) = 0x00000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int copyLSB(int x) {
  return (x << 31) >> 31;
  // makes the lsb the most significant via left shifting 31
  // and then right shifts back copying the msb(original lsb) to all bits
}
/*
 * evenBits - return word with all even-numbered bits set to 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 1
 */
int evenBits(void) {
  return (0 | (0x55) | (0x55 << 8) | (0x55 << 16) | (0x55 << 24));
  // 0x55 is 01010101 so this will set all even bits to 1,
  // just need to shift them 8 more bits each time
}
/*
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int logicalShift(int x, int n) {
  int ari_shift = x >> n;          // basic aritmetic shift right
  int mask = ~0 << (32 + ~n) << 1; // 32 + ~n = 31 - n, shifts 0xff to the left by 31 - n and then 1 more
  mask = ~mask;                    // inverts the mask
  return mask & ari_shift;         // apply mask
}
/*
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4
 */
int bang(int x) {
  int negative_x = (~x + 1);             // gets -x
  int sign_bit = (x | negative_x) >> 31; // check if x | -x has a sign bit
  // only 0 will not have a sign bit as ~0+1 = 0 and 0 | 0 = 0
  // all other numbers will have a sign bit
  return (sign_bit + 1); // flip the sign but to get the result
}
/*
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int leastBitPos(int x) {
  return x & (~x + 1);
  // invert and add 1 so that mask leads to just having a 1 at the least significant 1
  // 96 = 01100000
  //~96= 10011111
  //+1 = 10100000
  // 10100000 & 01100000 = 00100000
}
/*
 * isNotEqual - return 0 if x == y, and 1 otherwise
 *   Examples: isNotEqual(5,5) = 0, isNotEqual(4,5) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNotEqual(int x, int y) {
  return !!(x ^ y);
  // xor will return 0 if x == y
  // it will return anything else if not equal
  // make it always return 1 if not equal by using !!
  // 3 ^ 7 = 0b100 = 4 -> !!4 = 1
}
/*
 * negate - return -x
 *   Example: negate(1) = -1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int negate(int x) {
  x = ~x + 1;
  return x;
  // inverts and adds 1 to get the 2's complement, which is -x
}
/*
 * isPositive - return 1 if x > 0, return 0 otherwise
 *   Example: isPositive(-1) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 2
 */
int isPositive(int x) {
  return (!(x >> 31)) & (!!x);
  // first converts 0 to 1 of positives
  // and negatives to 0
  // then convert nonzero x to 1
}
/*
 * isNonNegative - return 1 if x >= 0, return 0 otherwise
 *   Example: isNonNegative(-1) = 0.  isNonNegative(0) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNonNegative(int x) {
  return !(x >> 31);
  // shift the sign bit to the right most and then invert it
}
/*
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
  int low = (x + ~0x30 + 1);
  // lower bound check, (x-0x30)
  int high = (0x39 + ~x + 1);
  // upper bound check (0x39-x)
  return !(low >> 31) & !(high >> 31);
  // if both are positive, then it is within the bounds
  // gets sign bit and inverts them
}
/*
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1,
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
  int sum = x + y;
  int x_sign = x >> 31;     // gets x sign
  int y_sign = y >> 31;     // gets y sign
  int sum_sign = sum >> 31; // gets sum sign
  return (!!(x_sign ^ y_sign)) | (!(x_sign ^ sum_sign) & !(y_sign ^ sum_sign));
  // checks if x and y had the same sign and then if the sign of the sum is different
}
/*
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
  int sign = x >> 31;       // gets the sign of x
  return (x + sign) ^ sign; // if x is negative,
  // then add 1 to x and then invert it to get positive
}
/*
 * isNonZero - Check whether x is nonzero using
 *              the legal operators except !
 *   Examples: isNonZero(3) = 1, isNonZero(0) = 0
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int isNonZero(int x) {
  return ((x | (~x + 1)) >> 31) & 0x01;
  // if -x and x are the same then x is 0
  // else x has a sign bit and is not 0
}
/*
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
  int n_byte = (x >> (n << 3)) & 0xFF;                   // gets nth byte
  int m_byte = (x >> (m << 3)) & 0xFF;                   // gets mth byte
  x = x & (~(0xFF << (n << 3))) & (~(0xFF << (m << 3))); // clears the nth and mth byte
  x = x | (n_byte << (m << 3)) | (m_byte << (n << 3));   // sets the nth and mth byte
  return x;
}
/*
 * bitMask - Generate a mask consisting of all 1's
 *   lowbit and highbit
 *   Examples: bitMask(5,3) = 0x38
 *   Assume 0 <= lowbit <= 31, and 0 <= highbit <= 31
 *   If lowbit > highbit, then mask should be all 0's
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int bitMask(int highbit, int lowbit) {
  int high_mask = ~0 << highbit << 1;       // gets the high mask
  int low_mask = ~0 << lowbit;              // gets the low mask
  return (high_mask ^ low_mask) & low_mask; // xor the 2 masks
  // and mask it with the low mask
}
